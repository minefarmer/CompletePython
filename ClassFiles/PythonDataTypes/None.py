'''                 None data type
None is a data type, that means nothing in it yet, or it or we've purposely set it to nothing.
'''
wallet = None  # It has nothing in it.

if wallet is None:
    print("There is nothing in my wallet")  # There is nothing in my wallet
    
    wallet = 82.45
print("My wallet has ", wallet)  # My wallet has  82.45
